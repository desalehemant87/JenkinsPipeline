# This is program for simple calculator operations.


class calc():
    # Addition of numbers
    def add(x,y):
        return x+y
    # Substraction of numbers
    def sub(x,y):
        return x-y
    # Multiplication of numbers
    def mult(x,y):
        return x*y
    #Division of numbers
    def div(x,y):
        return x/y

